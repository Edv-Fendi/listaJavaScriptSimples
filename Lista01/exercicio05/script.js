// Uma confecção produz X blusas de lã e para isto gasta uma certa quantidade de novelos.Faça um algoritmo para calcular quantos novelos de lã ela gasta por blusa

const blusa = prompt("Digite a quantidade de blusas feitas: ");
const la = prompt("Digite a quantidade de la usada: ");

const result = blusa / la;

console.log("O total de lã gasto em cada blusa foi de: " + result);